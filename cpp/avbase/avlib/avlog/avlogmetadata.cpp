#include "avlogmetadata.h"

/////////////////////////////////////////////////////////////////////////////

AVLogMetadataBase::AVLogMetadataBase(const QString &name) :
    m_name(name)
{
}

/////////////////////////////////////////////////////////////////////////////

AVLogMetadataBase::~AVLogMetadataBase()
{

}

/////////////////////////////////////////////////////////////////////////////

QString AVLogMetadataBase::getName() const
{
    return m_name;
}

/////////////////////////////////////////////////////////////////////////////

AVLogMetadata::AVLogMetadata()
{

}

/////////////////////////////////////////////////////////////////////////////

AVLogMetadata &AVLogMetadata::operator=(const AVLogMetadata &other)
{
    if (&other != this)
    {
        m_metadata_ptr = other.m_metadata_ptr;
    }
    return *this;
}

/////////////////////////////////////////////////////////////////////////////

QString AVLogMetadata::getName() const
{
    return m_metadata_ptr->getName();
}

/////////////////////////////////////////////////////////////////////////////

QString AVLogMetadata::getValueAsString() const
{
    return m_metadata_ptr->getValueAsString();
}

